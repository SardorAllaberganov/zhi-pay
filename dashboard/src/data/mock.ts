/**
 * Throughput — 60 per-minute points covering the last hour (09:31..10:30).
 * Shape: gentle ramp → mid-window dip → late-window peak.
 */
function buildThroughput60m(): { t: string; count: number }[] {
  const points: { t: string; count: number }[] = [];
  const start = new Date(now.getTime() - 59 * 60 * 1000);
  for (let i = 0; i < 60; i++) {
    const ts = new Date(start.getTime() + i * 60 * 1000);
    const hh = String(ts.getUTCHours()).padStart(2, '0');
    const mm = String(ts.getUTCMinutes()).padStart(2, '0');
    // Composite curve: base 18 + slow ramp + mid dip + late surge + light noise.
    const ramp = i * 0.35;
    const dip = i >= 20 && i < 32 ? -((i - 20) * 0.6) : 0;
    const surge = i >= 40 ? Math.pow(i - 40, 1.15) * 0.55 : 0;
    const noise = ((i * 53) % 9) - 4;
    const count = Math.max(2, Math.round(18 + ramp + dip + surge + noise));
    points.push({ t: `${hh}:${mm}`, count });
  }
  return points;
}

export const THROUGHPUT_60M = buildThroughput60m();

/**
 * FX client_rate over the last 24 hours, hourly samples.
 * Realistic micro-movement around 1404 UZS/CNY.
 */
function buildFxRate24h(): { t: string; rate: number }[] {
  const points: { t: string; rate: number }[] = [];
  const start = new Date(now.getTime() - 23 * 60 * 60 * 1000);
  for (let i = 0; i < 24; i++) {
    const ts = new Date(start.getTime() + i * 60 * 60 * 1000);
    const hh = String(ts.getUTCHours()).padStart(2, '0');
    // Low-amplitude oscillation around 1404 with a slight upward drift.
    const drift = i * 0.05;
    const wave = Math.sin(i / 3) * 4 + Math.cos(i / 5) * 2;
    const rate = Math.round((1402 + drift + wave) * 100) / 100;
    points.push({ t: `${hh}:00`, rate });
  }
  return points;
}

export const FX_RATE_24H = buildFxRate24h();

